/**
 * [ ] Use o operador && (E) para verificar se 10 > 5 e 8 > 2.
 */

console.log(10 > 5 && 8 > 2);
console.log(10 > 15 && 8 > 6);
//no operador e basta um ser falso, o resto retorna falso
//no operador ou basta um ser verdadeiro e o resto retorna verdadeiro

