/**
 * [ ] Use o operador || (OU) para verificar se 5 > 10 ou 10 > 5.
 */

console.log(5 > 10 || 10 > 5);
console.log(10 < 1 || 10 < 5);
/**
 * Correção Amigável: > No operador OU (||), é exatamente o oposto: basta que UMA condição seja verdadeira para o resultado ser true.
Ele só retorna false se todas as condições
 forem falsas (como aconteceu no teu segundo console.log).
 */
//No && (E): O computador procura por um false. Se encontrar um, para tudo e diz que é falso.

//No || (OU): O computador procura por um true. Se encontrar um, para tudo e diz que é verdadeiro.